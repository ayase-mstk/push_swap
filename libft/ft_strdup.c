#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*p;
	size_t	size;
	size_t	i;

	i = 0;
	size = ft_strlen(src);
	p = malloc(sizeof(char) * (size + 1));
	if (p == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		p[i] = src[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
