#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	s_size;

	s_size = ft_strlen(src);
	if (s_size + 1 < dstsize)
	{
		ft_memcpy(dst, src, s_size + 1);
	}
	else if (dstsize != 0)
	{
		ft_memcpy(dst, src, dstsize - 1);
		dst[dstsize - 1] = '\0';
	}
	return (s_size);
}
